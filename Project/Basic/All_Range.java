public class All_Range {
    public static void main(String[] args) {
       
        // integer range size

        System.out.println("\nInteger range " + Integer.MIN_VALUE + " to " + Integer.MAX_VALUE);

        // size of byte in java

        System.out.println("\nByte range:=>  " + Byte.MIN_VALUE + " to " + Byte.MAX_VALUE);
        
        // range of long

        System.out.println("\nLong range:=> " + Long.MIN_VALUE + " to " + Long.MAX_VALUE);

        // float range

        System.out.println("\nFloat range:=> " + Float.MIN_VALUE + " to " + Float.MAX_VALUE);

        // double range

        System.out.println("\nDouble range:=> " + Double.MIN_VALUE + " to " + Double.MAX_VALUE);
    }
}
